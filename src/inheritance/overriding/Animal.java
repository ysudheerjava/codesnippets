package inheritance.overriding;

public class Animal {
	
	public void makeSound() {
		System.out.println("Animal makes sound");
	}
	public void eats() {
		System.out.println("Animal can eat");
	}
	public void sleeps() {
		System.out.println("Animal can sleep");
	}
	public void walk() {
		System.out.println("Animal makes sound");
	}
	
	public void run() {
		System.out.println("Animal can run");
	}


}
