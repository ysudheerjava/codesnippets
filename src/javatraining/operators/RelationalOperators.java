package javatraining.operators;

public class RelationalOperators {

	public static void main(String[] args) {

		int a = 5;
		int b = 10;

		// Equal to (==)
		boolean isEqual = (a == b);
		System.out.println(isEqual);

		// Equal to (!=)
		boolean isNotEqual = (a != b);
		System.out.println(isNotEqual);

		// Greater than(>)
		boolean isGreaterThan = (a > b);
		System.out.println(isGreaterThan);

		// Less than(<)
		boolean isLessThan = (a < b);
		System.out.println(isLessThan);

		// Greater than or Equal to (>=)
		boolean isGreaterThanOrEqualTo = (a >= b);
		System.out.println(isGreaterThanOrEqualTo);

		// Less than or Equal to(<=)
		boolean isLessThanOrEqualTo = (a <= b);
		System.out.println(isLessThanOrEqualTo);

	}

}
