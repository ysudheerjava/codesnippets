package javatraining.operators;

//Program demonstrating the usage of Arithmetic operators
public class ArithmeticOperatorsDemo {
	
	
	public static void main(String[] args) {

		int a = 10;
		int b = 3;

		// Addition
		int sum = a + b;
		System.out.println("Sum: " + sum);

		// Subraction
		int difference = a - b;
		System.out.println("Difference: " + difference);

		// Multiplication
		int product = a * b;
		System.out.println("Product: " + product);

		// Division
		int quotient = a / b;
		System.out.println("Division: " + quotient);

		// Modulus (Remainder)
		int remainder = a % b;
		System.out.println("Remainder: " + remainder);

		
		System.out.println("********");
		// Increment (++)
		int c = 5;
		c++; // Increments by 1
		System.out.println("Incremented c : " + c);
		

		// Decrement (--)
		int d = 8;
		d--;
		System.out.println("Decremented d: " + d);

	
		System.out.println();
		// Pre-increment operator
		int i = 1;
		int j = ++i;
		System.out.println("Value of i " + i);// 2
		System.out.println("Value of j " + j);// 2
	
		
		
		// Post-increment operator
		int x = 1;
		int y = x++;
		System.out.println("Value of x " + x);// 2
		System.out.println("Value of y " + y);// 1
		
		
		System.out.println("*********");
		// Pre-decrement operator
		int k = 2;
		int l = --k;
		System.out.println("Value of k " + k);// 1
		System.out.println("Value of l " + l);// 1

		// Post-decrement operator
		int m = 2;
		int n = m--;
		System.out.println("Value of m " + m);// 1
		System.out.println("Value of n " + n);// 2
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
